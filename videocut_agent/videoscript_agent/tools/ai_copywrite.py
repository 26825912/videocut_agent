def parse_dify_response(dify_output_data):
    """
    解析 Dify 工作流输出的 JSON 数据。
    
    参数:
        dify_output_data (dict): Dify API 返回的原始完整字典
        
    返回:
        tuple: (解析后的完整字典结构, 串联后的口播文案字符串)
    """
    try:
        result_str = dify_output_data.get("data", {}).get("outputs", {}).get("result", "")
        
        if not result_str:
            raise ValueError("无法在响应中找到 data.outputs.result 字段")

        parsed_result = json.loads(result_str)
    
        timeline = parsed_result.get("script_timeline", [])
        
        script_list = [
            item.get("script_content", "").strip() 
            for item in timeline 
            if item.get("script_content")
        ]
        full_script = " ".join(script_list)
        
        return parsed_result, full_script

    except json.JSONDecodeError as e:
        logger.error(f"解析内部 result 字符串失败: {e}")
        return {}, ""
    except Exception as e:
        logger.error(f"处理数据时发生错误: {e}")
        return {}, ""


def ai_copywrite(breakdown_data,topic : str,time : str = "20-25",lang : str = "英文") -> dict:
    """
    以阻塞模式运行 Dify 工作流
    参数:
        api_key (str): Dify API 密钥
        inputs (dict): 工作流输入参数，默认为空字典
        user_id (str): 用户标识符，默认为 "abc-123"
    返回:
        dict: 工作流执行结果（JSON 解析后的字典）
    异常:
        requests.exceptions.RequestException: 网络请求失败
        ValueError: 无效的 API 响应
    """
    layer1 = breakdown_data.get("first_layer", "")
    layer2 = breakdown_data.get("second_layer", "")
    layer3 = breakdown_data.get("third_layer", "")
    inputs = {
        "layer1": str(layer1),
        "layer2": str(layer2),
        "layer3": str(layer3),
        "topic": topic,
        "time": time,
        "lang": lang
    }
    # 准备请求头
    headers = {
        "Authorization": f"Bearer {AI_COPYWRITE_API_KEY}",
        "Content-Type": "application/json"
    }
    
    # 准备请求体（使用阻塞模式）
    payload = {
        "inputs": inputs or {},  # 允许传入自定义输入
        "response_mode": "blocking",  # 关键修改：流式 -> 阻塞
        "user": "abc-123"
    }
    
    try:
        # 发送 POST 请求
        response = requests.post(
            AI_COPYWRITE_API_BASE_URL,
            headers=headers,
            data=json.dumps(payload),
            timeout=300 
        )
        
        response.raise_for_status()
        
        dify_response = response.json()
        parsed_result, full_script = parse_dify_response(dify_response)
        return parsed_result, full_script
    
    except requests.exceptions.RequestException as e:
        # 处理网络/HTTP错误
        error_detail = ""
        if hasattr(e, 'response') and e.response is not None:
            try:
                error_detail = e.response.json().get('message', '')
            except:
                error_detail = e.response.text[:200]
        raise Exception(f"API 请求失败: {str(e)} | 详情: {error_detail}") from e
    
    except json.JSONDecodeError as e:
        raise ValueError(f"无效的 API 响应: {response.text[:500]}") from e



